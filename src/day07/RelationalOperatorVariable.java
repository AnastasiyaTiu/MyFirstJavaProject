package day07;

public class RelationalOperatorVariable {
    public static void main(String[] args) {
        // > >= < <= == !=

        // Relation operator is used to compare 2 oprands
        // and return true or false
        // anything action or expression
        // that return true or false result
        //  can be called boolean expression
        int myNumber=10;

        System.out.println(myNumber > 5);
        System.out.println(myNumber <- 19);
        System.out.println(myNumber >= 5);
        System.out.println(myNumber >= 8);
        System.out.println(myNumber <= 5);
        System.out.println(myNumber >= 8);

        //  == is used to check equality
        //  AND VERY DIFFERENT FROM SINGE =

        // difference between == and = same as difference between equal and equality in daily English
        System.out.println(5 == 5);

        // != is used to check for inequality, ! in programming translate to NOT
        System.out.println(6 != 10);
        System.out.println(6!=6);
    }
}
